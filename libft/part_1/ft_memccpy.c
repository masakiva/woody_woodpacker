#include <stddef.h>

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t	i;
	void	*ret;

	i = 0;
	ret = NULL;
	while (i < n)
	{
		((unsigned char *)dst)[i] = ((unsigned char *)src)[i];
		if (((unsigned char *)src)[i] == (unsigned char)c)
		{
			ret = (void *)(dst + i + 1);
			break ;
		}
		i++;
	}
	return (ret);
}
